public class ItemCart {
    private String kodeCart, namaCart,hargaCart,jenisCart,ketCart,qtyCart,subtotalCart;

    public String getKodeCart() {
        return kodeCart;
    }

    public void setKodeCart(String kodeCart) {
        this.kodeCart = kodeCart;
    }

    public String getNamaCart() {
        return namaCart;
    }

    public void setNamaCart(String namaCart) {
        this.namaCart = namaCart;
    }

    public String getHargaCart() {
        return hargaCart;
    }

    public void setHargaCart(String hargaCart) {
        this.hargaCart = hargaCart;
    }

    public String getJenisCart() {
        return jenisCart;
    }

    public void setJenisCart(String jenisCart) {
        this.jenisCart = jenisCart;
    }

    public String getKetCart() {
        return ketCart;
    }

    public void setKetCart(String ketCart) {
        this.ketCart = ketCart;
    }

    public String getQtyCart() {
        return qtyCart;
    }

    public void setQtyCart(String qtyCart) {
        this.qtyCart = qtyCart;
    }

    public String getSubtotalCart() {
        return subtotalCart;
    }

    public void setSubtotalCart(String subtotalCart) {
        this.subtotalCart = subtotalCart;
    }

}
